
public class Cidade {
	
	private int codCid = 0;
	private String nomeCid = "";
	
	
	public int getCodCid() {
		return codCid;
	}
	public void setCodCid(int codCid) {
		this.codCid = codCid;
	}
	public String getNomeCid() {
		return nomeCid;
	}
	public void setNomeCid(String nomeCid) {
		this.nomeCid = nomeCid;
	}	

}
